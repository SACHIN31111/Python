# A set in Python is an unordered, mutable collection of unique elements. It is commonly used to store distinct values and perform mathematical set operations like union, intersection, and difference.
set={2,4,4,5,6,7,5,5,0,0}
print(set)



# Creating a set using curly braces {}
numbers = {1, 2, 3, 4, 5}

# Creating a set using the set() constructor
unique_chars = set("hello")  # {'h', 'e', 'l', 'o'}

# Creating an empty set (must use set(), not {})
empty_set = set()  # {} would create an empty dictionary

print(3 in numbers)  # Output: True
print(6 in numbers)  # Output: False
